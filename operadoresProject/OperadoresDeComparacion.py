# Ejemplo operadores de comparacion
m = int(input(" Ingrese el valor de m : "))
n = int(input(" Ingrese el valor de n : "))

print(f"la igualdad de {m} y {n}  es : {(m == n)}") #Igualdad
print(m > n) #Mayor que
print(m < n) #Menor que
print(m >= n) #Mayor igual que
print(m <= n) #Menor igual que
print(m != n) #No igual



