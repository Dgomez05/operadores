# E jemplos utilizando operasdores aritmeticos
numero1 = int (input( " Ingrese el numero1 " ))
numero2 = int (input( " Ingrese el numero2 " ))

suma = numero1 + numero2
resta = numero1 - numero2
division = numero1 / numero2
multiplicacion = numero1 * numero2
modulo = numero1 % numero2
divisionEntera = numero1 // numero2

print(f"La suma de {numero1} y {numero2} es : ", str(suma))
print(f"La resta de {numero1} y {numero2} es : ", str(resta))
print(f"La division de {numero1} y {numero2} es : ", str(division))
print(f"La multiplicacion de {numero1} y {numero2} es : ", str(multiplicacion))
print(f"La modulo de {numero1} y {numero2} es : ", str(modulo))
print(f"La divisionEntera es : ", str(divisionEntera))
