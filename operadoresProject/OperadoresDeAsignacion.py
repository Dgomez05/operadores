# Ejemplos de operadores de asignacion
var1 = 5
var2 = 12
var3 = 8
var4 = 14
var5 = 6
var6 = 8

var1 += 3 # var1 + 3 asignacion con suma
var2 -= 3 # var1 - 3 asignacion con resta
var3 *= 3 # var1 * 3 asignacion con multiplicacion
var4 /= 3 # var1 / 3 asignacion con division
var5 %= 3 # var1 % 3 asignacion con residuo
var6 //= 3 # var1 // 3 asignacion con division entera

print(f"Asignacion con suma : {var1}")
print(f"Asignacion con resta : {var2}")
print(f"Asignacion con multiplicacion : {var3}")
print(f"Asignacion con division : {var4}")
print(f"Asignacion con residuo : {var5}")
print(f"Asignacion con division entera  : {var6}")

